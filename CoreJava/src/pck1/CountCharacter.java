package pck1;

public class CountCharacter {
	
		  public static void main(String[] args) {  			
			int c='m';  		
			int count=0;  			 
			String name="madam";  			  
			for(int i=0;i<name.length();i++){  			  
			if(c==name.charAt(i)){  			  
			count++;  			  
			}  

			}  

			System.out.println(count);  

			}  

			} 